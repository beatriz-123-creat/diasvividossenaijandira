public class DiasVividos {

    public static void main(String[] args){
        // variáveis e cálculos para o contador de dias vividos.
        // CONSTANTES_SÃO_MAIUSCULAS;
         byte idadeDoUsuario = 23;

         final short DIAS_DO_ANO_FISCAL = 365;

         int diasVividos = idadeDoUsuario * DIAS_DO_ANO_FISCAL;

        // leitura e visualizacao de dados para o cliente;
        System.out.println("Você já viveu aproximadamente " + diasVividos + " dias!");

    }
}
